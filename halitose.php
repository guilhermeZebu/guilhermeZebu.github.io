                Nossos pacientes tem <b>características diversas</b> e um <b>mesmo problema</b>. O método de abordagem deverá ser individualizado de acordo com a sua classificação.<br><br>
                A <b>halitose</b> ou <b>mau hálito</b> atinge cerca de 30% da população do Brasil e acomete crianças, jovens, adultos e idosos, de ambos os gêneros e todas as classes sociais.<br><br>
                Por se tratar de um sinal e/ou um sintoma de <b>desequilíbrio</b> seja por alterações sistêmicas, locais ou hábitos de vida, torna-se necessário um <b>conhecimento mais abrangente</b> das reais condições do nosso paciente.<br><br>
                Para o <b>desenvolvimento deste método</b>, tivemos a preocupação de entrarmos em contato com a psicologia cognitiva, com métodos de nutrição,  conhecimento na área de estomatologia, estudos de doenças sistêmicas que tem repercussão na cavidade bucal e principalmente, a busca da observação que nos permite desenvolver um  protocolo sistematizados de questionamentos técnicos.<br>
            </p>
            <ul>
                <li><b>Anamnese</b> : O paciente irá preencher um questionário informativo de todo seu histórico médico e odontológico</li>
