                <li><b>Halitometria</b> : exame que determina a intensidade da propagação do hálito</li>
                <li><b>Organoletica</b> : percepção do examinador. No aparelho monitor de mensuração dos níveis de compostos sulfurados voláteis provenientes da cavidade bucal– halimeter –</li>
                <li><b>Avaliação dos padrões  salivares</b> : cor, fluxo, turbidez, viscosidade e pH</li>
                <li><b>Exames por imagem</b></li>
                <li><b>Avaliação dos hábitos alimentares</b></li>
